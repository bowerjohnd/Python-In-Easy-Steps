# Numbers in various expressions changed 
# from book example for improved clarity

a = 8
b = 2

print("Addition:\t", a, "+", b, "=", a + b)

print("Subtraction:\t", a, "-", b, "=", a - b)

print("Multiplication:\t", a, "x", b, "=", a * b)

print("Division:\t", a, "/", 3, "=", a / 3)
print("Floor Division:\t", a, "//", 3, "=", a // 3)

print("Modulo:\t\t", a, "%", b, "=", a % b)
print("Modulo:\t\t", 13, "%", 7, "=", 13 % 7)

print("Exponent:\t ", a, "^2 = ", a ** 2, sep = '')
